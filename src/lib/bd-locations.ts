/** Returns all division names. */
export function getDivisions(): string[] {
  return BD_LOCATIONS.map((d) => d.name);
}

/** Returns all district names for a given division name. */
export function getDistricts(divisionName: string): string[] {
  const division = BD_LOCATIONS.find((d) => d.name === divisionName);
  return division ? division.districts.map((d) => d.name) : [];
}

/** Returns all thana names for a given division + district. */
export function getThanas(
  divisionName: string,
  districtName: string,
): string[] {
  const division = BD_LOCATIONS.find((d) => d.name === divisionName);
  if (!division) return [];
  const district = division.districts.find((d) => d.name === districtName);
  return district ? district.thanas : [];
}
